n = int(input())

for i in range(n):
    number = input()
    num = int(number)
    found = False
    for candidate in [num - 1, num + 1]:
        s = str(candidate).zfill(6)
        sum1 = int(s[0]) + int(s[1]) + int(s[2])
        sum2 = int(s[3]) + int(s[4]) + int(s[5])
        if sum1 == sum2:
            found = True
    if found:
        print("Yes")
    else:
        print("No")
