# последних трёх цифр номера билета (номер у билета шестизначный).
# Оказалось, что суммы отличаются ровно на единицу. «Я в одном шаге от счастья», — подумал Вова,
# — «или предыдущий или следующий билет точно счастливый». Прав ли он?
# Входной файл содержит в первой строке число K – количество тестов.
# В следующих K строках записаны номера билетов. Количество тестов не больше 10.
# Номер состоит ровно из шести цифр, среди которых могут быть и нули.
# Гарантируется, что Вова умеет считать, то есть суммы первых трех цифр и последних трех цифр
# отличаются ровно на единицу.
# Выходной файл должен содержать K строк, в каждой из которых для соответствующего теста
# следует указать "Yes", если Вова прав, и "No", если нет.

