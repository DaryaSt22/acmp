# В единственной строке входного файла записано 6 натуральных чисел A1, A2, A3, B1, B2, B3,
# записанных в одной строке через пробел. Все числа не превосходят 100.
# В единственную строку выходного файла нужно вывести единственное целое число
# – сумму в рублях, которую смогут сотрудники заработать в случае наилучшего для себя заполнения емкостей песком.

# Проверка на примере
# Вход: 1 2 3 3 2 1 → A=(1,2,3), B=(3,2,1)
# Интуитивно: самый дорогой песок (3 руб) → в самую большую ёмкость (3 кг), и т.д.
# 3×3 + 2×2 + 1×1 = 9 + 4 + 1 = 14 ✓


# Генерирует все возможные перестановки элементов из заданного списка.
from itertools import permutations

a1, a2, a3, b1, b2, b3 = map(int, input().split())

results = []
A = [a1, a2, a3]
B = [b1, b2, b3]
for p in permutations([0, 1, 2]):
    # print(p)
    result = A[p[0]] * B[0] + A[p[1]] * B[1] + A[p[2]] * B[2]
    results.append(result)

print(max(results))
