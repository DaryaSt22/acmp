# Как и многие другие девочки, Маша любит разные гадания. Некоторое время назад
# Маша узнала новый способ гадать на числах – для какого-нибудь интересующего ее натурального числа
# n надо посчитать сумму всех чисел, на которые n делится без остатка.
# Маша не очень любит арифметику, и попросила вас написать программу,
# которая автоматизирует процесс гадания.
# В единственной строке входного файла
# записано натуральное число n (n ≤ 1000), которое Маша была вынуждена сообщить.
# Выведите сумму всех натуральных делителей числа n.

number = int(input())
total_even = sum(i for i in range(1, number + 1) if number % i == 0)
print(total_even)

# range(1, number + 1) — перебираем все числа от 1 до number включительно.
# for i in range(...) — переменная i принимает каждое из этих значений по очереди.
# if number % i == 0 — условие: берём i только если оно делит number без остатка
# (% — это остаток от деления).
# Например, для number = 6: 6 % 1 = 0 ✓, 6 % 2 = 0, 6 % 3 = 0,
# 6 % 4 = 2 ✗, 6 % 5 = 1 ✗, 6 % 6 = 0 → делители: 1, 2, 3, 6.
# sum(...) — суммирует все отобранные делители. Для number = 6 это 1 + 2 + 3 + 6 = 12.